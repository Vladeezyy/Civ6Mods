function OnChichenItzaCompleted(iX, iY, buildingType, playerID, cityID, percentComplete, WeJustDontKnow)
	if buildingType == GameInfo.Buildings[iBuilding].Index then
		if percentComplete < 100 then return end
		print("Chichen Itza was built this turn")
		local pPlot = Map.GetPlot(iX, iY)
		local isNW = 0;
		for direction = 0, DirectionTypes.NUM_DIRECTION_TYPES - 1, 1  do
			local adjacentPlot = Map.GetAdjacentPlot(iX, iY, direction);
			if adjacentPlot:IsNaturalWonder() then isNW = isNW + 1 end
		end 
		print("Chichen Itza surrounded by " .. isNW .. " Natural Wonders")
		if isNW > 0 then
			pPlot:SetProperty("CenotedChichenItza", 1)
		end
	end		
end		

Events.BuildingChanged.Add(OnChichenItzaCompleted)




