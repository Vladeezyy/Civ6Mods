-- CORE FUNCTIONS
--==========================================================================================================================
-- Modifier on Project Production
--==========================================================================================================================
function CityProductionUpdated_CheckProject( playerID:number, cityID:number, eProductionType, eProductionObject)
	local pPlayer = Players[playerID]
	if pPlayer == nil then return end
	local pCity = pPlayer:GetCities():FindID(cityID)
	if pCity == nil then return end
	local pCurrentBuild = pCity:GetBuildQueue():CurrentlyBuilding()
	local pPlot = Map.GetPlot(pCity:GetX(), pCity:GetY())
	if pCurrentBuild == iProject then
		print("Player " .. playerID .. " Currently producing the required project")
		pPlot:SetProperty(iProject, 1)
	else
		pPlot:SetProperty(iProject, 0)
	end
end

Events.CityProductionChanged.Add(CityProductionUpdated_CheckProject);
Events.CityProjectCompleted.Add(CityProductionUpdated_CheckProject);
Events.CityProductionUpdated.Add(CityProductionUpdated_CheckProject);	

